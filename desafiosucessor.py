n = int(input("digite aqui um número:"))
print(f"O sucessor de {n} é equivalente à {n + 1}")
print(f"O antecessor de {n} é equivalente à {n - 1}")
print(f"O dobro de {n} é equivalente à {n * 2}")
print(f"O triplo de {n} é equivalente à {n * 3}")
print(f"O valor de {n} divido por 3 é igual à {n / 3:.2f}")

