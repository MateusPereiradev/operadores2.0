nome = input("digite aqui seu nome:")
print("O tipo primitivo da variável nome é:", type(nome))
print("tem número?", nome.isnumeric())
print("é alfabetico?", nome.isalpha())
print("é alfanúmerico?", nome.isalnum())
print("é maiusculo?", nome.isupper())
print("é minusculo?", nome.islower())
print("começa com maiusculo?", nome.isupper())
print("é um espaço?", nome.isspace())
print("está capitalizada?", nome.istitle())

